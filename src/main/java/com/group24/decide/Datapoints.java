package com.group24.decide;

/**
 * Class to create 2 dimensional data points
 */
public class Datapoints {

    double x;
    double y;

    /**
     * Constructor to create a datapoint
     * @param x x-axis
     * @param y y-axis
     */
    public Datapoints(double x, double y){
        this.x = x;
        this.y = y;
    }
}
